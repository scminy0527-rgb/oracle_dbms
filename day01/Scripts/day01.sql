-- 관계
CREATE SEQUENCE SEQ_USER;
CREATE TABLE TBL_USER(
	ID NUMBER CONSTRAINT PK_USER PRIMARY KEY,
	USER_NAME VARCHAR2(255) NOT NULL,
	USER_ADDRESS VARCHAR2(255) NOT NULL,
	USER_PHONE VARCHAR2(255) DEFAULT '010-0000-0000'
);

CREATE SEQUENCE SEQ_ORDER;
CREATE TABLE TBL_ORDER(
	ID NUMBER CONSTRAINT PK_ORDER PRIMARY KEY,
	ORDER_PRODUCT_NAME VARCHAR2(255),
	ORDER_STOCK NUMBER DEFAULT 1,
	USER_ID NUMBER,
	CONSTRAINT FK_ORDER_USER FOREIGN KEY(USER_ID)
	REFERENCES TBL_USER(ID)
);



-- 상품 테이블(TBL_PRODUCT)
-- 상품 이름, 상품 가격, 상품 재고

-- 선물 테이블(TBL_GIFT)
-- 선물 주소, 선물 수량, 상품의 아이디

CREATE SEQUENCE SEQ_PRODUCT;
CREATE TABLE TBL_PRODUCT(
	ID NUMBER CONSTRAINT PK_PRODUCT PRIMARY KEY,
	PRODUCT_NAME VARCHAR2(255) NOT NULL,
	PRODUCT_PRICE NUMBER NOT NULL,
	PRODUCT_STOCK NUMBER DEFAULT 0
);

CREATE SEQUENCE SEQ_GIFT;
CREATE TABLE TBL_GIFT(
	ID NUMBER CONSTRAINT PK_GIFT PRIMARY KEY,
	GIFT_ADDRESS VARCHAR2(255) NOT NULL,
	GIFT_COUNT NUMBER NOT NULL,
	PRODUCT_ID NUMBER,
	CONSTRAINT FK_GIFT_PRODUCT FOREIGN KEY(PRODUCT_ID)
	REFERENCES TBL_PRODUCT(ID)
);




