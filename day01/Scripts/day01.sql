-- 자동차 테이블(TBL_CAR)
-- 이름 (NAME), 브랜드(BRAND), 가격(PRICE), 컬러(COLOR), 출고일(CREATE_AT) 
-- 인승(SEATER)
-- 제약조건

CREATE SEQUENCE SEQ_CAR;
CREATE TABLE TBL_CAR(
	ID NUMBER CONSTRAINT PK_CAR PRIMARY KEY,
	CAR_NAME VARCHAR2(255) NOT NULL,
	CAR_BRAND VARCHAR(255) NOT NULL,
	CAR_PRICE NUMBER NOT NULL,
	CAR_COLOR VARCHAR2(255) NOT NULL,
	CAR_CREATE_AT TIMESTAMP DEFAULT CURRENT_TIMESTAMP,
	CAR_SEATER NUMBER NOT NULL
);

-- 1. CAR_NAME 컬럼 이름 변경 -> CAR_MODEL
ALTER TABLE TBL_CAR RENAME COLUMN CAR_NAME TO CAR_MODEL;

-- 2. CAR_PRICE 타입 -> VARCHAR2(255)
ALTER TABLE TBL_CAR MODIFY CAR_PRICE VARCHAR2(255);
-- 만약 타입이 안바뀌면 삭제 한 다음에 추가 하는거도 가능

-- 3. COLOR 컬럼 삭제
ALTER TABLE TBL_CAR DROP COLUMN CAR_COLOR;


-- 제약조건을 수정
-- NOT NULL,
ALTER TABLE TBL_CAR MODIFY CAR_MODEL NULL;
ALTER TABLE TBL_CAR MODIFY CAR_MODEL NOT NULL;


