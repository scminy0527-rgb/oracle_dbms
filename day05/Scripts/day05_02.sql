-- 유저(TBL_BUY_USER)
-- 이름(BUY_USER_NAME), 나이(BUY_USER_AGE), 성별(BUY_USER_GENDER), 주소(BUY_USER_ADDRESS), 휴대폰(BUY_USER_PHONE)
CREATE SEQUENCE SEQ_BUY_USER;
CREATE TABLE TBL_BUY_USER(
   ID NUMBER CONSTRAINT PK_BUY_USER PRIMARY KEY,
   BUY_USER_NAME VARCHAR2(255) NOT NULL,
   BUY_USER_AGE NUMBER,
   BUY_USER_GENDER CHAR(6),
   BUY_USER_ADDRESS VARCHAR2(255) NOT NULL,
   BUY_USER_PHONE VARCHAR2(255)
);

-- 상품(TBL_SELL_PRODUCT)
-- 상품명(SELL_PRODUCT_NAME), 가격(SELL_PRODUCT_PRICE), 브랜드(SELL_PRODUCT_BRAND), 재고(SELL_PRODUCT_STOCK)
CREATE SEQUENCE SEQ_SELL_PRODUCT;
CREATE TABLE TBL_SELL_PRODUCT(
   ID NUMBER CONSTRAINT PK_SELL_PRODUCT PRIMARY KEY,
   SELL_PRODUCT_NAME VARCHAR2(255) NOT NULL,
   SELL_PRODUCT_PRICE NUMBER DEFAULT 0,
   SELL_PRODUCT_BRAND VARCHAR2(255),
   SELL_PRODUCT_STOCK NUMBER DEFAULT 999
);

-- 구매(TBL_PRODUCT_ORDER)
-- 요청사항(PRODUCT_ORDER_CONTENT), 구매일시(PRODUCT_ORDER_CREATE_AT)
CREATE SEQUENCE SEQ_PRODUCT_ORDER;
CREATE TABLE TBL_PRODUCT_ORDER(
   ID NUMBER CONSTRAINT PK_TBL_PRODUCT_ORDER PRIMARY KEY,
   BUY_USER_ID NUMBER,
   SELL_PRODUCT_ID NUMBER,
   CONSTRAINT FK_PRODUCT_ORDER_BUY_USER FOREIGN KEY(BUY_USER_ID)
   REFERENCES TBL_BUY_USER(ID),
   CONSTRAINT FK_PRODUCT_ORDER_SELL_PRODUCT FOREIGN KEY(SELL_PRODUCT_ID)
   REFERENCES TBL_SELL_PRODUCT(ID)
);

