-- 서울에 거주자의 1인당 평균 소비 지출액 조회
-- 1. 주문건 내 1인 전체 구매 금액에 대한 평균
SELECT TBU.ID, SUM(TSP.SELL_PRODUCT_PRICE) AS TOTAL
FROM TBL_PRODUCT_ORDER TPO
JOIN TBL_BUY_USER TBU
ON TPO.BUY_USER_ID = TBU.ID
	AND TBU.BUY_USER_ADDRESS LIKE '%서울%'
JOIN TBL_SELL_PRODUCT TSP
ON TPO.SELL_PRODUCT_ID = TSP.ID
GROUP BY TBU.ID;

SELECT AVG(TOTAL)
FROM (
	SELECT TBU.ID, SUM(TSP.SELL_PRODUCT_PRICE) AS TOTAL
	FROM TBL_PRODUCT_ORDER TPO
	JOIN TBL_BUY_USER TBU
	ON TPO.BUY_USER_ID = TBU.ID
		AND TBU.BUY_USER_ADDRESS LIKE '%서울%'
	JOIN TBL_SELL_PRODUCT TSP
	ON TPO.SELL_PRODUCT_ID = TSP.ID
	GROUP BY TBU.ID
);


-- 2. 1인당 주문 할 때 평균 금액
SELECT TBU.ID, ROUND(AVG(TSP.SELL_PRODUCT_PRICE)) AS AVG_PRICE
FROM TBL_PRODUCT_ORDER TPO
JOIN TBL_BUY_USER TBU
ON TPO.BUY_USER_ID = TBU.ID
	AND TBU.BUY_USER_ADDRESS LIKE '%서울%'
JOIN TBL_SELL_PRODUCT TSP
ON TPO.SELL_PRODUCT_ID = TSP.ID
GROUP BY TBU.ID;

SELECT ROUND(AVG(AVG_PRICE))
FROM (
	SELECT TBU.ID, AVG(TSP.SELL_PRODUCT_PRICE) AS AVG_PRICE
	FROM TBL_PRODUCT_ORDER TPO
	JOIN TBL_BUY_USER TBU
	ON TPO.BUY_USER_ID = TBU.ID
		AND TBU.BUY_USER_ADDRESS LIKE '%서울%'
	JOIN TBL_SELL_PRODUCT TSP
	ON TPO.SELL_PRODUCT_ID = TSP.ID
	GROUP BY TBU.ID
);













