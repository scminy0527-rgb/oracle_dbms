-- 1. 유저 중 나이가 30살이고 성별이 남자인 사람의 이름 조회
SELECT *
FROM TBL_BUY_USER
WHERE BUY_USER_AGE = 30 AND BUY_USER_GENDER LIKE '%남%';

-- 2. 성별이 여자이면서 경기도에 살고 있는 사용자가 구매한 상품 목록 조회
SELECT *
FROM TBL_BUY_USER
WHERE BUY_USER_GENDER LIKE '%여%' AND BUY_USER_ADDRESS LIKE '%경기도%';

SELECT TBU.BUY_USER_NAME, TBS.SELL_PRODUCT_NAME, TBU.BUY_USER_ADDRESS
FROM TBL_PRODUCT_ORDER TBP
JOIN (
	SELECT *
	FROM TBL_BUY_USER
	WHERE BUY_USER_GENDER LIKE '%여%' AND BUY_USER_ADDRESS LIKE '%경기도%'
) TBU
ON TBP.BUY_USER_ID = TBU.ID
JOIN TBL_SELL_PRODUCT TBS 
ON TBP.SELL_PRODUCT_ID = TBS.ID;


-- 3. 브랜드 '무신사'를 선호하는 나이대 조회 // 나이대 중에서 가장 무신사를 많이 구매한 나이대
SELECT TBU.BUY_USER_AGE || '세' AS "무신사 좋아하는 나이대"
FROM TBL_PRODUCT_ORDER TBP
JOIN TBL_SELL_PRODUCT TBS 
ON TBP.SELL_PRODUCT_ID = TBS.ID
	AND TBS.SELL_PRODUCT_BRAND LIKE '%무신사%'
JOIN TBL_BUY_USER TBU
ON TBP.BUY_USER_ID = TBU.ID
GROUP BY TBU.BUY_USER_AGE
ORDER BY COUNT(TBU.BUY_USER_AGE) DESC
FETCH FIRST 1 ROWS ONLY;


-- 4. 아디다스의 총 매출 조회
SELECT TO_CHAR(SUM(TSP.SELL_PRODUCT_PRICE), '999,999')   || '원' AS "아디다스 총 매출"
FROM TBL_PRODUCT_ORDER TPO
JOIN TBL_SELL_PRODUCT TSP
ON TPO.SELL_PRODUCT_ID = TSP.ID
	AND TSP.SELL_PRODUCT_BRAND LIKE '%아디다스%'
GROUP BY TSP.SELL_PRODUCT_PRICE;


-- 5. 평균 재고보다 작은 상품 조회 
